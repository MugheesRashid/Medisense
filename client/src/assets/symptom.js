// Get symptoms for a specific body part (with hierarchy mapping)
export const getSymptomsByBodyPart = (bodyPartName) => {
  // Get mapped categories for this body part
  const mappedCategories = symptomsDatabase.mapBodyPartToSymptomCategories(bodyPartName);
  
  return symptomsDatabase.symptoms.filter(symptom => {
    // Check if symptom has any of the mapped categories
    const hasMappedCategory = mappedCategories.some(category => 
      symptom.bodyParts.includes(category)
    );
    
    // IMPORTANT: Exclude symptoms that are ONLY general (isGeneral: true)
    // UNLESS they also have other specific body parts
    const isPureGeneral = symptom.isGeneral && 
                         symptom.bodyParts.length === 1 && 
                         symptom.bodyParts[0] === "general";
    
    return hasMappedCategory && !isPureGeneral;
  });
};

// Get symptoms by multiple body parts (enhanced)
export const getSymptomsByMultipleBodyParts = (bodyParts) => {
  const allMappedCategories = new Set();
  
  // Map each body part to its symptom categories
  bodyParts.forEach(part => {
    const categories = symptomsDatabase.mapBodyPartToSymptomCategories(part);
    categories.forEach(cat => allMappedCategories.add(cat));
  });
  
  const mappedCategories = Array.from(allMappedCategories);
  
  return symptomsDatabase.symptoms.filter(symptom => {
    // Check if symptom has any of the mapped categories
    const hasMappedCategory = mappedCategories.some(category => 
      symptom.bodyParts.includes(category)
    );
    
    // Exclude pure general symptoms when specific body part is selected
    const isPureGeneral = symptom.isGeneral && 
                         symptom.bodyParts.length === 1 && 
                         symptom.bodyParts[0] === "general";
    
    return hasMappedCategory && !isPureGeneral;
  });
};

