export const searchSymptoms = (query) => {
  const lowerQuery = query.toLowerCase();
  return symptomsDatabase.symptoms.filter(
    (symptom) =>
      symptom.name.toLowerCase().includes(lowerQuery) ||
      symptom.description.toLowerCase().includes(lowerQuery) ||
      symptom.bodyParts.some(part => part.toLowerCase().includes(lowerQuery))
  );
};

